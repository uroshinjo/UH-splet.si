<h3>Kako do spletne strani?</h3>
<p style="width:700px; font-size:14px; color:#2f383f;">Ko se odločite za spletno stran, je naslednji korak poiskati osebo, ki vam bo to spletno stran postavila. Ponujam vam tri osnovne pakete za hitro ter učinkovito postavitev spletne strani.</p>
<h3>Kaj je kaj?</h3>
<h5>Statična spletna stran</h5>
<p style="width:700px; margin-top:-25px; font-size:14px; color:#2f383f;">
Na statični strani vsebine uporabnik ne more spreminjati, lahko jo spreminja samo programer. Večina spletnih strani je statičnih, saj pri predstavitvi podjetja ni potrebe po konstantnem
posodabljanju vsebine.
</p>

<h5>Dinamična spletna stran</h5>
<p style="width:700px; margin-top:-25px; font-size:14px; color:#2f383f;">
Pri dinamični strani uporabnik sam ureja vsebin s sistemom za upravljanje vsebin. Najlepši primer so novice na strani, ki jih dodajajo uporabniki sami.
</p>

<h5>Sistem za upravljanje vsebin</h5>
<p style="width:700px; margin-top:-25px; font-size:14px; color:#2f383f;">
Je sistem, ki omogoča urejanje in vzdrževanje vsebine spletnih strani brez znanja označevalnega jezika HTML. Urednik spletne strani tako lahko samostojno spreminja besedila, slike in druge elemente spletne strani brez pomoči podjetja ali osebe, ki je stran izdelala.
</p>
